def escape_po(s: str) -> str:
    return s.replace("\\", "\\\\").replace('"', '\\"').replace("\n", "\\n").replace("\t", "\\t")


def write_po(lang: str, table: dict[str, str]) -> Path:
    po_dir = HERE / lang / "LC_MESSAGES"
    po_dir.mkdir(parents=True, exist_ok=True)
    po_path = po_dir / f"{DOMAIN}.po"

    lines: list[str] = [
        "# File Converter — translation catalog",
        f"# Language: {lang}",
        "#",
        'msgid ""',
        'msgstr ""',
        '"Project-Id-Version: fileconverter 1.1.0\\n"',
        '"PO-Revision-Date: 2026-04-17 00:00+0000\\n"',
        '"Last-Translator: File Converter project\\n"',
        f'"Language-Team: {lang}\\n"',
        f'"Language: {lang}\\n"',
        '"MIME-Version: 1.0\\n"',
        '"Content-Type: text/plain; charset=UTF-8\\n"',
        '"Content-Transfer-Encoding: 8bit\\n"',
        "",
    ]
    for msgid in STRINGS:
        msgstr = table.get(msgid, "")
        lines.append(f'msgid "{escape_po(msgid)}"')
        lines.append(f'msgstr "{escape_po(msgstr)}"')
        lines.append("")
    po_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return po_path


def compile_mo(po_path: Path) -> Path:
    mo_path = po_path.with_suffix(".mo")
    subprocess.run(
        ["msgfmt", "-o", str(mo_path), str(po_path)], check=True
    )
    return mo_path


def main() -> None:
    missing_any = False
    for lang, table in TRANSLATIONS.items():
        missing = [s for s in STRINGS if s not in table]
        if missing:
            missing_any = True
            print(f"{lang}: WARNING — missing {len(missing)} translations", file=sys.stderr)
            for m in missing[:3]:
                print(f"    {m!r}", file=sys.stderr)
        po = write_po(lang, table)
        mo = compile_mo(po)
        rel = mo.relative_to(HERE.parent)
        print(f"  {lang:<10} → {rel}")
    if missing_any:
        sys.exit(1)


if __name__ == "__main__":
    main()
